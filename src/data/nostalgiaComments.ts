export const getRandomComment = (songId: string): string => {
  const songComments = nostalgiaComments.find((c) => c.songId === songId);
  if (!songComments || songComments.comments.length === 0) {
    const genericComments = [
      '这旋律一响起，时光仿佛倒流了',
      '当年的卡带都快听磨花了',
      '谁的青春里没有这首歌呢',
      '收音机里循环播放的那些日子',
      '听到这首歌就想起了好多往事',
      '那个年代的歌，每首都是故事',
      '录音机里反复倒带听的旋律',
      '一晃眼，这么多年过去了',
    ];
    return genericComments[Math.floor(Math.random() * genericComments.length)];
  }
  const index = Math.floor(Math.random() * songComments.comments.length);
  return songComments.comments[index];
};
