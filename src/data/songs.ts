export const getSongsByYear = (year: number): Song[] => {
  return mockSongs.filter((song) => song.year === year);
};

export const getSongsByDecade = (decade: 80 | 90): Song[] => {
  const start = decade === 80 ? 1980 : 1990;
  const end = decade === 80 ? 1989 : 1999;
  return mockSongs.filter((song) => song.year >= start && song.year <= end);
};

export const getYears = (): number[] => {
  const years = new Set(mockSongs.map((song) => song.year));
  return Array.from(years).sort((a, b) => a - b);
};

export const getRandomSong = (): Song => {
  const index = Math.floor(Math.random() * mockSongs.length);
  return mockSongs[index];
};
